#include "main.h"
/**
 * add - Entry point
 * Description: 'adding'
 * @c: int 1
 * @b: int 2
 * Return: an int
 */
int add(int c, int b)
{
	return (c + b);
}

/**
 * sub - Entry point
 * Description: 'subtracting'
 * @c: int 1
 * @b: int 2
 * Return: an int
 */
int sub(int c, int b)
{
	return (c - b);
}

/**
 * mul - Entry point
 * Description: 'multiplying'
 * @c: int 1
 * @b: int 2
 * Return: an int
 */
int mul(int c, int b)
{
	return (c * b);
}

/**
 * div - Entry point
 * Description: 'dividing'
 * @c: int 1
 * @b: int 2
 * Return: an int
 */
int div(int c, int b)
{
	return (c / b);
}

/**
 * mod - Entry point
 * Description: 'remainder'
 * @c: int 1
 * @b: int 2
 * Return: an int
 */
int mod(int c, int b)
{
	return (c % b);
}
