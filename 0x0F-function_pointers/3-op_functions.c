/**
* op_add - entry point
* Description: 'addition'
*
* @a: int input
* @b: int input
* Return: an int
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - entry point
 * Description: 'subtraction'
 *
 * @a: int input
 * @b: int input
 * Return: an int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - entry point
 * Description: 'multiplication'
 *
 * @a: int input
 * @b: int input
 * Return: an int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - entry point
 * Description: 'division'
 *
 * @a: int input
 * @b: int input
 * Return: an int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - entry point
 * Description: 'remainder'
 *
 * @a: int input
 * @b: int input
 * Return: an int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
